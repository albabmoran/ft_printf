#include "ft_printf.h"

int	ft_putstr(char *s, int countb)
{
	int	i;

	if (!s)
	{
		write(1, "(null)", 6);
		countb += 6;
	}
	else
	{
		i = 0;
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
			countb++;
		}
	}
	return (countb);
}
